# contadores com for
print("contador de 0 a 9:")
for x in range(10):
    print(x, end=" ")
    
print("\n\ncontador de 10 a 20:")
for x in range(10,21):
    print(x,end=" ")
    
print("\n\ncontador de 5 a 50 com passo 5:")
for x in range(5,51,5):
    print(x,end=" ")
    
print("\n\ncontador de 10 a 1:")
for x in range(10,0,-1):
    print(x,end=" ")
    
print("\n\ncontador de 100 a 0 com passo -10:")
for x in range(100,-1,-10):
    print(x,end=" ")
    

